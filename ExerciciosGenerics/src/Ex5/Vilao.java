package Ex5;

public class Vilao {
    private String nome;
    private String planoMaligno;

    public Vilao(String nome, String planoMaligno) {
        this.nome = nome;
        this.planoMaligno = planoMaligno;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getPlanoMaligno() {
        return planoMaligno;
    }

    public void setPlanoMaligno(String planoMaligno) {
        this.planoMaligno = planoMaligno;
    }
}
